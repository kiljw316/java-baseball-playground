package mini;

public class Calculator {

    private final int result = 0;

    public Calculator() {
    }

    public Integer sum(int a, int b) {
        return a + b;
    }

    public Integer sub(int a, int b) {
        return a - b;
    }

    public Integer mul(int a, int b) {
        return a * b;
    }

    public Integer div(int a, int b) {
        return a / b;
    }

    public Integer mod(int a, int b) {
        return a % b;
    }
}
